-- Grant full access to Admins for 'visitors' and 'badges' tables

-- 1. Visitors Access
CREATE POLICY "Admins can manage visitors"
ON visitors
FOR ALL
TO authenticated
USING (
  EXISTS (
    SELECT 1 FROM users
    WHERE users.id = auth.uid()
    AND users.role = 'admin'
  )
)
WITH CHECK (
  EXISTS (
    SELECT 1 FROM users
    WHERE users.id = auth.uid()
    AND users.role = 'admin'
  )
);

-- 2. Badges Access
